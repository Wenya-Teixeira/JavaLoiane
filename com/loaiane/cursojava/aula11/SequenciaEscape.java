package com.loaiane.cursojava.aula11;

public class SequenciaEscape {

	public static void main(String[] args) {
		
		//para soltar uma linha = \n
		// "Hello, World!", ou seja, que colocar as aspas duplas na frase
		System.out.print("\"Hello, World!\" \n");
		
		// 1\4, se utiliza o \\ para que vá 1\4
		System.out.println("1\\4");
	}

}
