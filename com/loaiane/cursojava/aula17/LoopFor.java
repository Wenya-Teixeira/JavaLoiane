package com.loaiane.cursojava.licaodecasa.aula17;

public class LoopFor {

	public static void main(String[] args) {
		//Adicionar 1
		for (int i = 0; i < 5; i++) {
			System.out.println("i tem valor = " + i);
		}
		//Subtrair 1
		for (int i = 5; i >= 0; i--) {
			System.out.println("i tem valor = " + i);
		}

	}

}
