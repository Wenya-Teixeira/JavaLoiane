package com.loaiane.cursojav.aula20;

public class MatrizasParte1 {

	public static void main(String[] args) {

		// Matriz = ltabela de n x m elementos
		// Problema: armazenar as 4 notas do ano de 30 alunos

		double[][] notasAlunos = new double[30][4];

		// primeiro aluno
		notasAlunos[0][0] = 10;
		notasAlunos[0][1] = 7;
		notasAlunos[0][2] = 9;
		notasAlunos[0][3] = 9.5;

		// segundo aluno
		notasAlunos[1][0] = 9;
		notasAlunos[1][1] = 8;
		notasAlunos[1][2] = 7;
		notasAlunos[1][3] = 9;

		// terceiro aluno
		notasAlunos[2][0] = 8;
		notasAlunos[2][1] = 9;
		notasAlunos[2][2] = 10;
		notasAlunos[2][3] = 7;

	}

}
